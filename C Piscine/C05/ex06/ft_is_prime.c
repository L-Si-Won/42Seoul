int	ft_is_prime(int nb)
{
	int	n;

	if (nb <= 1)
		return (0);
	n = 2;
	while (n * n <= nb)
	{
		if (nb % n == 0)
			return (0);
		if (n >= 46341)
			break ;
		n++;
	}
	return (1);
}
